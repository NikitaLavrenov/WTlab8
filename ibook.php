    <p>
21 июля 1999 года Стив Джобс представил впервые iBook G3 во время конференции Macworld Expo в Нью-Йорке.
Компьютер был представлен, как изящное, недорогое устройство для школьников, студентов и частных пользователей.
iBook был изначально представлен в двух цветах: Рыжий (Tangerine) и Голубой (Blueberry) и предлагал необычный и инновационный дизайн, а также высокие технические характеристики на тот момент.
Компания была намерена таким образом выйти из кризиса и укрепить снова свои позиции на рынке.
Главной фразой на рекламе нового устройства была iMac to Go.
Изначальная цена устройства на территории США составляла 1599$, это было на 900$ дешевле, чем модель Apple PowerBook, предназначенная для профессионалов, однако обладающая более скромными функциями.
В iBook были встроены стерео динамики, слот для SD карты, ИК-порт, микрофон, аудио-порт SCSI порт. В 2000 году была выпущена обновлённая модель с FireWire-портом, видеовыходом, DVD приводом и процессором с тактовой частотой 466 МГц.
